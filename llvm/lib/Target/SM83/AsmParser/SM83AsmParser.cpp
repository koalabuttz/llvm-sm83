/// Maps from the set of all register names to a register number.
/// \note Generated by TableGen.
static MCRegister MatchRegisterName(StringRef Name);

/// Returns the SM83 condition code for the given name, or -1 if invalid.
static int matchCondCode(StringRef Name) {
  return StringSwitch<int>(Name.lower())
      .Case("nz", 0)
      .Case("z", 1)
      .Case("nc", 2)
      .Case("c", 3)
      .Default(-1);
}

namespace {

/// A parsed SM83 assembly operand.
class SM83Operand : public MCParsedAsmOperand {
  enum KindTy { k_Token, k_Register, k_Immediate } Kind;

public:
  SM83Operand(StringRef Tok, SMLoc const &S)
      : Kind(k_Token), Tok(Tok), Start(S), End(S) {}
  SM83Operand(MCRegister Reg, SMLoc const &S, SMLoc const &E)
      : Kind(k_Register), RegImm({Reg, nullptr}), Start(S), End(E) {}
  SM83Operand(const MCExpr *Imm, SMLoc const &S, SMLoc const &E)
      : Kind(k_Immediate), RegImm({0, Imm}), Start(S), End(E) {}

  struct RegisterImmediate {
    MCRegister Reg;
    const MCExpr *Imm;
  };
  union {
    StringRef Tok;
    RegisterImmediate RegImm;
  };

  SMLoc Start, End;

  void addRegOperands(MCInst &Inst, unsigned N) const {
    assert(Kind == k_Register && "Unexpected operand kind");
    assert(N == 1 && "Invalid number of operands!");
    Inst.addOperand(MCOperand::createReg(getReg()));
  }

  void addImmOperands(MCInst &Inst, unsigned N) const {
    assert(Kind == k_Immediate && "Unexpected operand kind");
    assert(N == 1 && "Invalid number of operands!");
    const MCExpr *Expr = getImm();
    if (!Expr)
      Inst.addOperand(MCOperand::createImm(0));
    else if (const auto *CE = dyn_cast<MCConstantExpr>(Expr))
      Inst.addOperand(MCOperand::createImm(CE->getValue()));
    else
      Inst.addOperand(MCOperand::createExpr(Expr));
  }

  void addCondCodeOperands(MCInst &Inst, unsigned N) const {
    addImmOperands(Inst, N);
  }

  bool isCondCode() const {
    if (!isImm())
      return false;
    const auto *CE = dyn_cast<MCConstantExpr>(getImm());
    if (!CE)
      return false;
    int64_t Val = CE->getValue();
    return Val >= 0 && Val <= 3;
  }

  bool isReg() const override { return Kind == k_Register; }
  bool isImm() const override { return Kind == k_Immediate; }
  bool isToken() const override { return Kind == k_Token; }
  bool isMem() const override { return false; }

  StringRef getToken() const {
    assert(Kind == k_Token && "Invalid access!");
    return Tok;
  }

  MCRegister getReg() const override {
    assert(Kind == k_Register && "Invalid access!");
    return RegImm.Reg;
  }

  const MCExpr *getImm() const {
    assert(Kind == k_Immediate && "Invalid access!");
    return RegImm.Imm;
  }

  static std::unique_ptr<SM83Operand> CreateToken(StringRef Str, SMLoc S) {
    return std::make_unique<SM83Operand>(Str, S);
  }

  static std::unique_ptr<SM83Operand> CreateReg(MCRegister Reg, SMLoc S,
                                                 SMLoc E) {
    return std::make_unique<SM83Operand>(Reg, S, E);
  }

  static std::unique_ptr<SM83Operand> CreateImm(const MCExpr *Val, SMLoc S,
                                                 SMLoc E) {
    return std::make_unique<SM83Operand>(Val, S, E);
  }

  SMLoc getStartLoc() const override { return Start; }
  SMLoc getEndLoc() const override { return End; }

  void print(raw_ostream &O, const MCAsmInfo &MAI) const override {
    switch (Kind) {
    case k_Token:
      O << "Token: \"" << getToken() << "\"";
      break;
    case k_Register:
      O << "Register: " << getReg().id();
      break;
    case k_Immediate:
      O << "Immediate: \"";
      MAI.printExpr(O, *getImm());
      O << "\"";
      break;
    }
    O << "\n";
  }
};

/// Parses SM83 assembly from a stream.
class SM83AsmParser : public MCTargetAsmParser {
  MCAsmParser &Parser;
  const MCRegisterInfo *MRI;

#define GET_ASSEMBLER_HEADER
#include "SM83GenAsmMatcher.inc"

  bool matchAndEmitInstruction(SMLoc IDLoc, unsigned &Opcode,
                               OperandVector &Operands, MCStreamer &Out,
                               uint64_t &ErrorInfo,
                               bool MatchingInlineAsm) override;

  bool parseRegister(MCRegister &Reg, SMLoc &StartLoc, SMLoc &EndLoc) override;
  ParseStatus tryParseRegister(MCRegister &Reg, SMLoc &StartLoc,
                               SMLoc &EndLoc) override;

  bool parseInstruction(ParseInstructionInfo &Info, StringRef Name,
                        SMLoc NameLoc, OperandVector &Operands) override;

  ParseStatus parseDirective(AsmToken DirectiveID) override;

  unsigned validateTargetOperandClass(MCParsedAsmOperand &Op,
                                      unsigned Kind) override;

  bool parseOperand(OperandVector &Operands);
  bool parseBracketedOperand(OperandVector &Operands);
  bool parseImmediate(OperandVector &Operands);

  MCRegister tryMatchRegister(StringRef Name);

public:
  SM83AsmParser(const MCSubtargetInfo &STI, MCAsmParser &Parser,
                const MCInstrInfo &MII, const MCTargetOptions &Options)
      : MCTargetAsmParser(Options, STI, MII), Parser(Parser) {
    MCAsmParserExtension::Initialize(Parser);
    MRI = getContext().getRegisterInfo();
    setAvailableFeatures(ComputeAvailableFeatures(STI.getFeatureBits()));
  }

  MCAsmParser &getParser() const { return Parser; }
  AsmLexer &getLexer() const { return Parser.getLexer(); }
};

} // end anonymous namespace

MCRegister SM83AsmParser::tryMatchRegister(StringRef Name) {
  MCRegister Reg = MatchRegisterName(Name);
  if (!Reg)
    Reg = MatchRegisterName(Name.lower());
  return Reg;
}

bool SM83AsmParser::matchAndEmitInstruction(SMLoc Loc, unsigned &Opcode,
                                            OperandVector &Operands,
                                            MCStreamer &Out,
                                            uint64_t &ErrorInfo,
                                            bool MatchingInlineAsm) {
  MCInst Inst;
  unsigned MatchResult =
      MatchInstructionImpl(Operands, Inst, ErrorInfo, MatchingInlineAsm);

  switch (MatchResult) {
  case Match_Success:
    Inst.setLoc(Loc);
    Out.emitInstruction(Inst, getSTI());
    return false;
  case Match_MnemonicFail:
    return Error(Loc, "invalid instruction");
  case Match_InvalidOperand: {
    SMLoc ErrorLoc = Loc;
    if (ErrorInfo != ~0ULL && ErrorInfo < Operands.size())
      ErrorLoc = Operands[ErrorInfo]->getStartLoc();
    return Error(ErrorLoc, "invalid operand for instruction");
  }
  case Match_MissingFeature:
    return Error(Loc,
                 "instruction requires a CPU feature not currently enabled");
  default:
    return true;
  }
}

bool SM83AsmParser::parseRegister(MCRegister &Reg, SMLoc &StartLoc,
                                  SMLoc &EndLoc) {
  StartLoc = getLexer().getLoc();
  if (getLexer().is(AsmToken::Identifier)) {
    Reg = tryMatchRegister(getLexer().getTok().getString());
    if (Reg) {
      EndLoc = getLexer().getTok().getEndLoc();
      Parser.Lex();
      return false;
    }
  }
  return Error(StartLoc, "expected register");
}

ParseStatus SM83AsmParser::tryParseRegister(MCRegister &Reg, SMLoc &StartLoc,
                                            SMLoc &EndLoc) {
  StartLoc = getLexer().getLoc();
  if (getLexer().is(AsmToken::Identifier)) {
    Reg = tryMatchRegister(getLexer().getTok().getString());
    if (Reg) {
      EndLoc = getLexer().getTok().getEndLoc();
      Parser.Lex();
      return ParseStatus::Success;
    }
  }
  return ParseStatus::NoMatch;
}

bool SM83AsmParser::parseInstruction(ParseInstructionInfo &Info, StringRef Name,
                                     SMLoc NameLoc, OperandVector &Operands) {
  Operands.push_back(SM83Operand::CreateToken(Name, NameLoc));

  if (getLexer().is(AsmToken::EndOfStatement)) {
    Parser.Lex();
    return false;
  }

  // For branch/return instructions, try parsing condition code first.
  // This resolves the "c" ambiguity: in jp/jr/call/ret context, "c" is the
  // carry condition code; in all other instructions, "c" is register C.
  if (Name == "jp" || Name == "jr" || Name == "call" || Name == "ret") {
    if (getLexer().is(AsmToken::Identifier)) {
      int CC = matchCondCode(getLexer().getTok().getString());
      if (CC >= 0) {
        AsmToken NextTok = getLexer().peekTok();
        if (NextTok.is(AsmToken::Comma) ||
            NextTok.is(AsmToken::EndOfStatement)) {
          SMLoc S = getLexer().getLoc();
          SMLoc E = getLexer().getTok().getEndLoc();
          const MCExpr *Expr = MCConstantExpr::create(CC, getContext());
          Operands.push_back(SM83Operand::CreateImm(Expr, S, E));
          Parser.Lex(); // consume condition code

          if (getLexer().is(AsmToken::Comma))
            Parser.Lex(); // consume comma

          if (getLexer().is(AsmToken::EndOfStatement)) {
            Parser.Lex();
            return false;
          }
          // Fall through to parse remaining operand (the branch target).
        }
      }
    }
  }

  // Parse operands.
  while (true) {
    if (parseOperand(Operands)) {
      Parser.eatToEndOfStatement();
      return true;
    }

    if (getLexer().is(AsmToken::EndOfStatement))
      break;

    if (getLexer().is(AsmToken::Comma))
      Parser.Lex();
  }

  Parser.Lex(); // consume EndOfStatement
  return false;
}

bool SM83AsmParser::parseOperand(OperandVector &Operands) {
  switch (getLexer().getKind()) {
  case AsmToken::Identifier: {
    StringRef Name = getLexer().getTok().getString();
    MCRegister Reg = tryMatchRegister(Name);

    if (Reg) {
      SMLoc S = getLexer().getLoc();
      SMLoc E = getLexer().getTok().getEndLoc();
      Operands.push_back(SM83Operand::CreateReg(Reg, S, E));
      Parser.Lex();
      return false;
    }

    // Not a register — parse as expression (label/symbol).
    return parseImmediate(Operands);
  }

  case AsmToken::LBrac:
    return parseBracketedOperand(Operands);

  case AsmToken::Plus:
    // Standalone '+' token (for the "sp + offset" pattern).
    Operands.push_back(SM83Operand::CreateToken(
        getLexer().getTok().getString(), getLexer().getLoc()));
    Parser.Lex();
    return false;

  case AsmToken::Minus:
  case AsmToken::Integer:
    return parseImmediate(Operands);

  default:
    return Error(getLexer().getLoc(), "unexpected token in operand");
  }
}

bool SM83AsmParser::parseBracketedOperand(OperandVector &Operands) {
  Operands.push_back(SM83Operand::CreateToken("[", getLexer().getLoc()));
  Parser.Lex(); // consume '['

  if (getLexer().is(AsmToken::Identifier)) {
    StringRef Name = getLexer().getTok().getString();
    MCRegister Reg = tryMatchRegister(Name);

    if (Reg) {
      SMLoc S = getLexer().getLoc();
      SMLoc E = getLexer().getTok().getEndLoc();
      Operands.push_back(SM83Operand::CreateReg(Reg, S, E));
      Parser.Lex();

      // Check for post-increment '+' or post-decrement '-'.
      if (getLexer().is(AsmToken::Plus) || getLexer().is(AsmToken::Minus)) {
        Operands.push_back(SM83Operand::CreateToken(
            getLexer().getTok().getString(), getLexer().getLoc()));
        Parser.Lex();
      }
    } else {
      // Not a register — parse as address expression.
      if (parseImmediate(Operands))
        return true;
    }
  } else {
    // Numeric or expression address (e.g., [0x1234]).
    if (parseImmediate(Operands))
      return true;
  }

  if (getLexer().isNot(AsmToken::RBrac))
    return Error(getLexer().getLoc(), "expected ']'");

  Operands.push_back(SM83Operand::CreateToken("]", getLexer().getLoc()));
  Parser.Lex(); // consume ']'
  return false;
}

bool SM83AsmParser::parseImmediate(OperandVector &Operands) {
  SMLoc S = getLexer().getLoc();
  const MCExpr *Expr;
  if (getParser().parseExpression(Expr))
    return true;
  SMLoc E = SMLoc::getFromPointer(getLexer().getLoc().getPointer() - 1);
  Operands.push_back(SM83Operand::CreateImm(Expr, S, E));
  return false;
}

ParseStatus SM83AsmParser::parseDirective(AsmToken DirectiveID) {
  return ParseStatus::NoMatch;
}

