// Uses MatchClassKind defined in SM83GenAsmMatcher.inc.
unsigned SM83AsmParser::validateTargetOperandClass(MCParsedAsmOperand &AsmOp,
                                                   unsigned ExpectedKind) {
  SM83Operand &Op = static_cast<SM83Operand &>(AsmOp);

  // When the matcher expects a fixed token (e.g., literal "hl" in
  // "ld\ta, [hl]") but we parsed a Register operand, check if the
  // register name matches the expected token string.
  if (Op.isReg()) {
    StringRef RegName = MRI->getName(Op.getReg());
    if (matchTokenString(RegName) == static_cast<MatchClassKind>(ExpectedKind))
      return Match_Success;
  }

  return Match_InvalidOperand;
}
